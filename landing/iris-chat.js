    function speakBrowserTTS(text) {
      if (!window.speechSynthesis) return;
      window.speechSynthesis.cancel();
      const sentences = text.match(/[^.!?]+[.!?]+/g) || [text];
      let i = 0;
      function next() {
        if (i >= sentences.length || !voiceEnabled) return;
        const utt = new SpeechSynthesisUtterance(sentences[i++]);
        const voice = getBestVoice();
        if (voice) utt.voice = voice;
        utt.rate = 0.94; utt.pitch = 1.0;
        utt.onend = () => setTimeout(next, 80);
        utt.onerror = () => {};
        window.speechSynthesis.speak(utt);
      }
      next();
    }

    function stopSpeaking() {
      // Resolve any pending awaits so the scenario engine doesn't hang
      ttsQueue.forEach(item => { if (item && item.done) try { item.done(); } catch(e){} });
      ttsQueue = [];
      if (currentAudio) {
        try {
          currentAudio.pause();
          currentAudio.removeAttribute('src');
          currentAudio.load(); // Reset the element cleanly
          // Trigger the playElevenLabsChunk pause-handler so its awaiting
          // promise resolves. Without this, runTTSQueue's `await` wedges
          // forever and ttsRunning stays true — silently breaking every
          // subsequent enqueueTTS() call.
          currentAudio.currentTime = 0;
        } catch(e) {}
        currentAudio = null;
      }
      if (window.speechSynthesis) window.speechSynthesis.cancel();
      irisIsSpeaking = false;
      // Belt and suspenders: if the runTTSQueue loop somehow stayed marked as
      // running (e.g. an in-flight playElevenLabsChunk that never resolved),
      // free it so the next enqueueTTS() can re-fire the queue.
      ttsRunning = false;
    }

