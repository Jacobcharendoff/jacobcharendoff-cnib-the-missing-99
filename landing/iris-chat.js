    function handleRealtimeEvent(ev) {
      let msg;
      try { msg = JSON.parse(ev.data); } catch { return; }
      switch (msg.type) {
        case 'input_audio_buffer.speech_started':
          setIrisStatus('listening', 'Listening…');
          break;
        case 'input_audio_buffer.speech_stopped':
          setIrisStatus('thinking', 'Thinking…');
          break;
        case 'conversation.item.input_audio_transcription.completed': {
          const userText = (msg.transcript || '').trim();
          if (userText) addUserMessage(userText);
          break;
        }
        case 'response.audio_transcript.delta': {
          if (!realtimeAssistantBubble) {
            realtimeAssistantText = '';
            realtimeAssistantBubble = addBotBubbleStreaming();
          }
          realtimeAssistantText += msg.delta || '';
          if (realtimeAssistantBubble) {
            setSafeText(realtimeAssistantBubble, realtimeAssistantText);
            chatBody.scrollTop = chatBody.scrollHeight;
          }
          setIrisStatus('speaking', 'Speaking…');
          break;
        }
        case 'response.audio_transcript.done':
        case 'response.done':
          realtimeAssistantBubble = null;
          realtimeAssistantText = '';
          setIrisStatus('listening', 'Listening — just talk');
          break;
        case 'error':
          console.warn('Realtime error:', msg.error);
          break;
      }
    }

