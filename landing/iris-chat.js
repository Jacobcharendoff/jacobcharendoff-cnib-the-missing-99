    // Auto-playing dual-voice scenario engine.
    // Total runtime target ≤ 90 seconds. Sequence:
    //   1. Briefing (narrator voice — sets the scene)
    //   2. Conversation script (alternating Iris + member voices)
    //   3. Mid-point insight (narrator steps out)
    //   4. Continue conversation
    //   5. Action card (visual only, no TTS — shows what Iris just did)
    //   6. Debrief (narrator wraps it)
    //   7. Next-step buttons
    async function playScenario(s) {
      // Demos now play as a live real experience. The pre-demo buffer/intro
      // card sets the stage. No narrator, no meta-commentary, no Iris self-intro.
      // The scripted conversation starts immediately.

      // Kick off prefetch of the first turn so it starts instantly
      const firstTurnAudioPromise = (s.script && s.script[0])
        ? prefetchTTSAudio(s.script[0].text, s.script[0].voice)
        : Promise.resolve(null);

      // 1. Call begins divider + first half of conversation (with prefetch pipeline)
      insertPhaseDivider('Connecting…');
      await wait(500);
      if (scenarioCancelled) return;

      // First turn's audio is already being fetched in the background
      let nextAudio = await firstTurnAudioPromise;
      for (let i = 0; i < s.script.length; i++) {
        if (scenarioCancelled) return;
        const turn = s.script[i];
        const currentAudioReady = nextAudio;
        // Kick off the next prefetch BEFORE we play current — overlap network with audio
        const nextTurn = s.script[i + 1];
        nextAudio = nextTurn ? prefetchTTSAudio(nextTurn.text, nextTurn.voice) : null;
        addScriptTurn(turn);
        await playPrefetched(currentAudioReady, turn.text);
        if (nextAudio && nextAudio.then) nextAudio = await nextAudio;
        await wait(180);
      }

      // 3. Mid-point narrator REMOVED — demo plays as a continuous live conversation.
      // Brief pause between halves keeps natural breath.
      if (scenarioCancelled) return;
      await wait(360);

      // 4. Second half of conversation (same prefetch pipeline)
      if (s.scriptAfterMid && s.scriptAfterMid.length) {
        let nextAudio2 = s.scriptAfterMid[0]
          ? await prefetchTTSAudio(s.scriptAfterMid[0].text, s.scriptAfterMid[0].voice)
          : null;
        for (let i = 0; i < s.scriptAfterMid.length; i++) {
          if (scenarioCancelled) return;
          const turn = s.scriptAfterMid[i];
          const currentAudioReady = nextAudio2;
          const nextTurn = s.scriptAfterMid[i + 1];
          nextAudio2 = nextTurn ? prefetchTTSAudio(nextTurn.text, nextTurn.voice) : null;
          addScriptTurn(turn);
          await playPrefetched(currentAudioReady, turn.text);
          if (nextAudio2 && nextAudio2.then) nextAudio2 = await nextAudio2;
          await wait(340);
        }
      }

      // 5. Action card — animates checkmarks one by one
      if (scenarioCancelled) return;
      await wait(420);
      insertPhaseDivider('iris. is acting now');
      await addActionCard(s.action);
      await wait(700);

      // 6. THEN / NOW transformation card — replaces the meta debrief.
      // The contrast IS the teaching. No narrator stepping out.
      if (scenarioCancelled) return;
      const memberLabel = (s.title || 'Member').split(',')[0];
      addThenNowCard(s.intro, memberLabel);
      await wait(900);

      // 7. Next steps
      if (scenarioCancelled) return;
      currentPhase = 'finished';
      stopCountdown();
      addNextStepButtons();
    }

