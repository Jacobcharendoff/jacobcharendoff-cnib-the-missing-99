    // ===== TTS PREFETCH (used by the scenario engine to eliminate inter-turn lag) =====
    // The default flow renders a bubble, then waits for /api/tts to round-trip
    // before any audio plays. That round-trip is the "weird delay" between
    // speakers. Prefetching one turn ahead means by the time we render the
    // bubble, the audio blob is already in memory and plays instantly.
    async function prefetchTTSAudio(text, voice = 'iris') {
      if (!voiceEnabled || !text) return null;
      const plain = text.replace(/<[^>]+>/g, '').replace(/\n/g, '. ').trim();
      if (!plain) return null;
      try {
        const response = await fetch('/api/tts', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ text: plain, voice })
        });
        if (!response.ok) return null;
        const blob = await response.blob();
        const url = URL.createObjectURL(blob);
        // Return a lightweight handle instead of a new Audio() object.
        // playPrefetched() will feed this URL to the persistent element.
        return { _objectUrl: url, _prefetched: true };
      } catch (e) {
        console.warn('TTS prefetch failed:', e.message);
        return null;
      }
    }

    // Fallback pacing when we have no audio (TTS disabled or failed).
    // Roughly models natural speaking pace so demo bubbles don't fire off
    // in sped-up mode. ~14 chars/sec ≈ 70ms/char, with sensible min/max.
    function computeReadMs(text) {
      const plain = (text || '').replace(/<[^>]+>/g, '').trim();
      const len = plain.length;
      if (!len) return 900;
      return Math.max(1400, Math.min(7000, Math.round(len * 58) + 600));
    }

    function playPrefetched(audioHandle, fallbackText) {
      return new Promise((resolve) => {
        if (!audioHandle) {
          // No audio available — pace the bubble against a reading-time
          // fallback so the conversation still feels live, not sped-up.
          irisIsSpeaking = true;
          const ms = computeReadMs(fallbackText);
          setTimeout(() => { irisIsSpeaking = false; resolve(); }, ms);
          return;
        }
        // Use the persistent Audio element instead of creating a new one.
        // This keeps the user-gesture "unlock" alive on mobile Safari.
        const audio = persistentAudio;
        const url = audioHandle._objectUrl;
        currentAudio = audio;
        irisIsSpeaking = true;
        let settled = false;
        const cleanup = () => {
          if (settled) return;
          settled = true;
          try { URL.revokeObjectURL(url); } catch(e){}
          if (currentAudio === audio) currentAudio = null;
          irisIsSpeaking = false;
          resolve();
        };
        audio.onplay  = () => { if (window.__dismissConnecting) window.__dismissConnecting(); };
        audio.onended = cleanup;
        audio.onerror = cleanup;
        // Mirrors the I2 fix in playElevenLabsChunk: stop() calls audio.pause()
        // which doesn't fire 'ended'. Without onpause, clicking Next mid-narrator-
        // beat leaves this promise wedged until the safety timer fires (~8s lag).
        audio.onpause = () => { if (!audio.ended && !settled) cleanup(); };
        // Safety net: if audio never actually starts/ends (blocked, 0-duration,
        // weird blob), fall back to reading-time pacing rather than instant resolve.
        const safetyMs = computeReadMs(fallbackText) + 1500;
        setTimeout(() => {
          if (!settled && (audio.paused || audio.ended || !audio.duration)) {
            cleanup();
          }
        }, safetyMs);
        audio.src = url;
        audio.play().catch(cleanup);
      });
    }

    // ---- Extend window.irisTour with prefetch + play ----
    // The tour needs to preload ALL audio on modal open (so scenes start
    // instantly) and then play pre-fetched blobs in a specific interleaved
    // order (narrator beat → dialogue turn → narrator beat → ...).
    // .prefetch(text, voice) returns a handle OR a Promise resolving to one.
    // .play(handle, fallbackText) plays it through persistentAudio, resolves
    // on ended. Fallback text is used for read-time pacing if audio is null.
    window.irisTour.prefetch = prefetchTTSAudio;
