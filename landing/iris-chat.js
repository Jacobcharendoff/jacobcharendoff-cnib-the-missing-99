    // === Sentence-streaming TTS queue ===
    // Each chunk is fetched + played in order. Lets us start speaking sentence 1
    // while sentence 2 is still being generated by the LLM.
    function enqueueTTS(text, voice = 'iris') {
      if (!text || !voiceEnabled) return;
      ttsQueue.push({ text, voice });
      if (!ttsRunning) runTTSQueue();
    }

    async function runTTSQueue() {
      ttsRunning = true;
      irisIsSpeaking = true;
      // Pause mic while iris. is talking so we don't transcribe ourselves
      pauseListeningForIris();
      while (ttsQueue.length > 0 && voiceEnabled) {
        const chunk = ttsQueue.shift();
        try {
          await playElevenLabsChunk(chunk.text, chunk.voice || 'iris');
        } catch (e) {
          console.warn('TTS chunk failed:', e.message);
        }
        if (chunk.done) chunk.done();
      }
      ttsRunning = false;
      irisIsSpeaking = false;
      resumeListeningForIris();
    }

    async function playElevenLabsChunk(text, voice = 'iris') {
      const response = await fetch('/api/tts', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ text, voice })
      });
      if (!response.ok) {
        // /api/tts already cascades ElevenLabs -> OpenAI server-side. If it
        // STILL failed, both upstream providers are down. Stay silent rather
        // than fall back to the robotic built-in speechSynthesis voice, which
        // makes Iris sound like a sluggish AI bot instead of a person.
        console.warn('[tts] both upstream providers failed — skipping speech');
        return;
      }
      const audioBlob = await response.blob();
      const audioUrl = URL.createObjectURL(audioBlob);
      const audio = persistentAudio;
      audio.src = audioUrl;
      audio.playbackRate = 1.0;
      currentAudio = audio;
      // Lazy Web Audio graph setup (must be inside gesture handler).
      // setVoiceGain normalizes loudness — margaret/priya arrive ~6dB
      // quieter than ash/shimmer from OpenAI; GainNode corrects that.
      ensureAudioGraph();
      setVoiceGain(voice);
      await new Promise((resolve) => {
        let settled = false;
        const done = () => {
          if (settled) return;
          settled = true;
          try { URL.revokeObjectURL(audioUrl); } catch(e) {}
          resolve();
        };
        audio.onplay  = () => { if (window.__dismissConnecting) window.__dismissConnecting(); };
        audio.onended = done;
        audio.onerror = done;
        // CRITICAL: stopSpeaking() pauses the audio element. Pause does NOT
        // fire 'ended' or 'error', so without this handler the awaiting promise
        // would wedge forever — leaving ttsRunning=true and silently breaking
        // every subsequent enqueueTTS() call (since runTTSQueue won't re-fire).
        // Fix: removed `currentTime === 0` guard — stop() may fire before the
        // first decoded frame, leaving currentTime=0. Without this fix the
        // promise wedges forever (ttsRunning stuck true, queue dead).
        // `settled` already prevents double-calling done().
        audio.onpause = () => { if (audio.ended || settled) return; done(); };
        audio.play().catch(done);
      });
      if (currentAudio === audio) currentAudio = null;
    }

