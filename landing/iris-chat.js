    //
    // OLD (buggy) behavior: typewriter fired char-by-char at a fixed 18ms
    // cadence and shipped sentence chunks to TTS as they completed. The
    // typewriter ran ahead of the voice (because TTS has ~600-1200ms network
    // latency per sentence) and there was dead air between sentences while
    // each fetch ran serially. Result: text-before-voice and choppy speech.
    //
    // NEW behavior: ONE /api/tts call for the whole response. Wait for the
    // audio blob, start playback, and drive the typewriter off
    // audio.currentTime / audio.duration so every character lands exactly
    // when it's being spoken. Text and voice arrive together; there is zero
    // gap between sentences because there's only one audio stream.
    //
    // Fallback paths:
    // - voiceEnabled === false: use the original char-by-char typewriter.
    // - TTS fetch fails: render the full text immediately, no voice.
    async function streamLLMResponse(data, bubbleEl, opts) {
      const fullText = (data && data.text) || '';
      const onReady = opts && typeof opts.onReady === 'function' ? opts.onReady : null;
      let readyFired = false;
      const fireReady = () => {
        if (readyFired) return;
        readyFired = true;
        try { onReady && onReady(); } catch (e) {}
      };

      // Reset/stop any in-flight speech before this turn
      stopSpeaking();

      const renderUpTo = (n) => {
        const clamped = Math.max(0, Math.min(fullText.length, n));
        setSafeText(bubbleEl, fullText.slice(0, clamped));
        chatBody.scrollTop = chatBody.scrollHeight;
      };

      // Voice-disabled path: classic char-by-char typewriter, no TTS.
      if (!voiceEnabled) {
        fireReady();
        let rendered = '';
        for (let i = 0; i < fullText.length; i++) {
          const ch = fullText[i];
          rendered += ch;
          setSafeText(bubbleEl, rendered);
          chatBody.scrollTop = chatBody.scrollHeight;
          let delay = 18;
          if (ch === ' ') delay = 10;
          else if (/[.!?]/.test(ch)) delay = 180;
          else if (/[,;:]/.test(ch)) delay = 90;
          await new Promise(r => setTimeout(r, delay));
        }
        conversationHistory.push({ role: 'assistant', content: fullText });
        try { logTurnToWebhook(fullText, data && data.moderated, data && data.reason); } catch (e) {}
        return fullText;
      }

      // Voice-enabled path: render text IMMEDIATELY so the bubble appears with
      // no lag, then fetch TTS audio in parallel. Previously we waited for the
      // TTS blob before showing any text, which stacked ~2-4s of silence on top
      // of the generation latency. Text-first feels instant; audio catches up.
      fireReady();
      if (window.__dismissConnecting) window.__dismissConnecting();
      renderUpTo(fullText.length);

      let audioPlayed = false;
      try {
        const ttsResponse = await fetch('/api/tts', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ text: fullText, voice: 'iris' })
        });

        if (ttsResponse.ok) {
          const audioBlob = await ttsResponse.blob();
          const audioUrl = URL.createObjectURL(audioBlob);
          const audio = persistentAudio;
          audio.src = audioUrl;
          audio.playbackRate = 1.0;
          currentAudio = audio;
          irisIsSpeaking = true;
          pauseListeningForIris();

          await new Promise((resolve) => {
            let settled = false;
            let rafId = null;
            const cleanup = () => {
              if (settled) return;
              settled = true;
              if (rafId) cancelAnimationFrame(rafId);
              try { URL.revokeObjectURL(audioUrl); } catch (e) {}
              resolve();
            };

            audio.onplay = () => {
              // Text is already fully rendered. Just mark that audio started.
              audioPlayed = true;
            };

            audio.onended = () => {
              renderUpTo(fullText.length);
              cleanup();
            };
            audio.onerror = () => {
              renderUpTo(fullText.length);
              cleanup();
            };
            // stopSpeaking() pauses the audio; make sure we settle the
            // awaiting promise so we don't wedge the caller forever.
            audio.onpause = () => {
              if (audio.currentTime === 0 || audio.ended || settled) return;
              cleanup();
            };

            audio.play().catch((err) => {
              console.warn('[tts] audio.play() blocked:', err && err.message);
              renderUpTo(fullText.length);
              cleanup();
            });
          });

          if (currentAudio === audio) currentAudio = null;
          irisIsSpeaking = false;
          resumeListeningForIris();
        } else {
          console.warn('[tts] /api/tts failed:', ttsResponse.status);
        }
      } catch (e) {
        console.warn('[tts] fetch threw:', e && e.message);
      }

      // If the audio path never actually played, render the text immediately
      // so the member still sees Iris's reply.
      if (!audioPlayed) {
        fireReady();
        renderUpTo(fullText.length);
      }

      conversationHistory.push({ role: 'assistant', content: fullText });

      // Fire-and-forget session logging to the Google Sheets webhook.
      try { logTurnToWebhook(fullText, data && data.moderated, data && data.reason); } catch (e) {}

      // Iris finished a real LLM turn — arm the silence check-in timer.
      try { resetIrisSilenceTimer(); } catch (e) {}

      return fullText;
    }

