    async function handleUserMessage(text) {
      stopSpeaking();
      addUserMessage(text);

      // ===== CRISIS DETECTION — runs first, before anything else =====
      const crisisKind = detectCrisis(text);
      if (crisisKind) {
        const crisisText = crisisResponse(crisisKind);
        conversationHistory.push({ role: 'user', content: text });
        const typing = showTypingIndicator();
        await new Promise(r => setTimeout(r, 600));
        const row = document.createElement('div');
        row.className = 'iris-msg-row bot';
        const bubble = document.createElement('div');
        bubble.className = 'iris-msg bot';
        bubble.innerHTML = '';
        row.appendChild(bubble);
        // Defer bubble append and typing removal until audio actually starts,
        // so the member doesn't stare at a blank bubble during the TTS fetch.
        const swapTypingToBubble = () => {
          try { typing.remove(); } catch (e) {}
          if (!row.parentNode) {
            chatBody.appendChild(row);
            chatBody.scrollTop = chatBody.scrollHeight;
          }
        };
        await streamLLMResponse(
          { text: crisisText, moderated: true, reason: 'crisis_' + crisisKind },
          bubble,
          { onReady: swapTypingToBubble }
        );
        swapTypingToBubble();
        // Log Iris turn to session pipeline — the streaming path bypasses addBotMessage
        // so we have to call logIrisEvent directly or transcripts end up member-only.
        if (typeof logIrisEvent === 'function') logIrisEvent('iris', crisisText, { crisis: crisisKind });
        const meta = document.createElement('div');
        meta.className = 'iris-msg-meta';
        meta.textContent = getTimeString();
        row.appendChild(meta);
        chatBody.scrollTop = chatBody.scrollHeight;
        conversationStep++;
        return;
      }

      // Auto-end scenario if user signals goodbye and we're inside a named conversation
      if (currentPhase === 'conversation' && /^(end scenario|end demo|stop scenario|that's it|that's enough|wrap up|debrief|i'm done|we're done)\b/i.test(text.trim())) {
        setTimeout(() => endScenarioWithDebrief(), 400);
        return;
      }

      // If Realtime voice is active, route the typed message through the data channel
      // so Iris responds with one voice and one model — no parallel TTS.
      if (realtimeActive && realtimeDC && realtimeDC.readyState === 'open') {
        sendRealtimeEvent({
          type: 'conversation.item.create',
          item: {
            type: 'message',
            role: 'user',
            content: [{ type: 'input_text', text }],
          },
        });
        sendRealtimeEvent({ type: 'response.create' });
        return;
      }

      const typing = showTypingIndicator();

      // Try LLM first
      if (USE_PROXY_CHAT) {
        try {
          const streamResponse = await callLLM(text, currentScenario);
          if (streamResponse) {
            // Create streaming bubble but DON'T append it or drop the typing
            // indicator until audio actually starts playing. This kills the
            // "empty bubble next to a typing indicator" flicker.
            const row = document.createElement('div');
            row.className = 'iris-msg-row bot';
            const bubble = document.createElement('div');
            bubble.className = 'iris-msg bot';
            bubble.innerHTML = '';
            row.appendChild(bubble);
            const swapTypingToBubble = () => {
              try { typing.remove(); } catch (e) {}
              if (!row.parentNode) {
                chatBody.appendChild(row);
                chatBody.scrollTop = chatBody.scrollHeight;
              }
            };

            const fullText = await streamLLMResponse(streamResponse, bubble, { onReady: swapTypingToBubble });
            // Safety net: if onReady never fired (shouldn't happen), force it now.
            swapTypingToBubble();

            // Log Iris turn to session pipeline. The streaming path does NOT
            // go through addBotMessage, so without this call the email
            // transcripts come out member-only (which is exactly what was
            // happening in Bob's April 9 session).
            if (typeof logIrisEvent === 'function') {
              logIrisEvent('iris', fullText, streamResponse && streamResponse.moderated ? { moderated: true, reason: streamResponse.reason } : null);
            }

            // Add timestamp
            const meta = document.createElement('div');
            meta.className = 'iris-msg-meta';
            meta.textContent = getTimeString();
            row.appendChild(meta);

            chatBody.scrollTop = chatBody.scrollHeight;
            // Note: speech is already streaming sentence-by-sentence from streamLLMResponse
            conversationStep++;
            return;
          }
        } catch (e) {
          console.warn('LLM streaming failed, falling back:', e.message);
        }
      }

      // Fallback if LLM is unavailable — keep it short and human.
      const delay = 600 + Math.random() * 800;
      setTimeout(() => {
        typing.remove();
        addBotMessage(
          "Sorry — I'm having trouble connecting to my brain right now. Can you try that again in a moment? If it keeps happening, you can always reach a real person at CNIB on 1-800-563-2642.",
          ["Try again", "I'll wait a moment"]
        );
        conversationStep++;
      }, delay);
    }

    function sendIrisMessage() {
      const text = chatInput.value.trim();
      if (!text) return;
      chatInput.value = '';
      handleUserMessage(text);
    }

